using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
using MarioPizzaWeb.Models;
using MarioPizzaOriginal.Domain;

namespace MarioPizzaWeb.Controllers
{
    public class HomeController : Controller
    {
        private readonly ILogger<HomeController> _logger;

        public HomeController(ILogger<HomeController> logger)
        {
            _logger = logger;
        }

        public IActionResult Index()
        {
            //ViewBag.Orders = DataAccess.OrderRepository.GetAll();
            ViewBag.Orders = new List<MarioPizzaOrder>() { new MarioPizzaOrder { ClientPhoneNumber = "123123123" }, new MarioPizzaOrder { ClientPhoneNumber="333444555" } };
            return View();
        }

        public IActionResult Privacy()
        {
            return View();
        }

        [ResponseCache(Duration = 0, Location = ResponseCacheLocation.None, NoStore = true)]
        public IActionResult Error()
        {
            return View(new ErrorViewModel { RequestId = Activity.Current?.Id ?? HttpContext.TraceIdentifier });
        }
    }
}
